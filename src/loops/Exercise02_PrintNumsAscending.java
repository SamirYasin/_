package loops;

public class Exercise02_PrintNumsAscending {
    public static void main(String[] args) {
        String s = "";
        for(int i = 1; i <= 10; i++){
            if (i == 10) {
                s += i;
                break;
            } s += i + " - ";
            }
        System.out.println(s);

        }
 }

