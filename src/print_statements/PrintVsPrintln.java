package print_statements;

public class PrintVsPrintln {
    public static void main(String[] args){

        //println
        //prints each statement as its own line
        System.out.println("This is println");
        System.out.println("Samir");
        System.out.println("Yasin");
        System.out.println("Today is Sunday");

        //print
        //prints all print statements on one line
        System.out.print("This is print");
        System.out.print("Samir");
        System.out.print("Yasin");
        System.out.print("Today is Sunday");
    }
}
